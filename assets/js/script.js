const setToggleLabelForState = (toggleEl, isExpanded) => {
  if (!toggleEl) return;
  const key = isExpanded ? 'common.toggle.less' : 'common.toggle.more';
  const label = resolveTranslation(currentLang, key) || '';
  toggleEl.textContent = label;
  toggleEl.setAttribute('aria-expanded', isExpanded ? 'true' : 'false');
};

const collapseDescriptionSection = (wrapperEl, toggleEl) => {
  if (!wrapperEl || !toggleEl) return;
  wrapperEl.classList.add('is-collapsed');
  wrapperEl.classList.remove('is-expanded');
  setToggleLabelForState(toggleEl, false);
};

const toggleDescriptionSection = (wrapperEl, toggleEl) => {
  if (!wrapperEl || !toggleEl || toggleEl.hidden) return;
  const willExpand = wrapperEl.classList.contains('is-collapsed');
  if (willExpand) {
    wrapperEl.classList.remove('is-collapsed');
    wrapperEl.classList.add('is-expanded');
  } else {
    wrapperEl.classList.add('is-collapsed');
    wrapperEl.classList.remove('is-expanded');
  }
  setToggleLabelForState(toggleEl, willExpand);
};

const refreshToggleLabel = (wrapperEl, toggleEl) => {
  if (!wrapperEl || !toggleEl || toggleEl.hidden) return;
  const isExpanded = wrapperEl.classList.contains('is-expanded');
  setToggleLabelForState(toggleEl, isExpanded);
};

const updateDescriptionVisibility = (wrapperEl, toggleEl, hasDescription, hideToggleOnly = false) => {
  if (wrapperEl) {
    wrapperEl.hidden = !hasDescription;
  }
  if (toggleEl) {
    toggleEl.hidden = !hasDescription || hideToggleOnly;
  }
};

const initCategoryHeroDescriptions = () => {
  const descriptionBlocks = document.querySelectorAll('[data-category-description]');
  descriptionBlocks.forEach((wrapper) => {
    const content = wrapper.querySelector('[data-category-description-content]');
    const toggle = wrapper.querySelector('[data-category-readmore]');
    if (!content || !toggle) return;

    toggle.addEventListener('click', (event) => {
      event.preventDefault();
      toggleDescriptionSection(wrapper, toggle);
    });

    categoryHeroDescriptionSections.push({ wrapper, toggle, content });
  });
  refreshCategoryHeroDescriptions();
};

const refreshCategoryHeroDescriptions = () => {
  categoryHeroDescriptionSections.forEach(({ wrapper, toggle, content }) => {
    if (!wrapper || !toggle || !content) return;
    const hasDescription = Boolean(content.textContent.trim()) || content.children.length > 0;
    updateDescriptionVisibility(wrapper, toggle, hasDescription);
    if (!hasDescription) {
      wrapper.classList.remove('is-collapsed', 'is-expanded');
      return;
    }

    const collapseHeight = Number(wrapper.dataset.collapseHeight) || 220;
    const shouldCollapse = content.scrollHeight > collapseHeight;
    updateDescriptionVisibility(wrapper, toggle, true, !shouldCollapse);

    if (!shouldCollapse) {
      wrapper.classList.add('is-expanded');
      wrapper.classList.remove('is-collapsed');
      return;
    }

    if (!wrapper.classList.contains('is-expanded') && !wrapper.classList.contains('is-collapsed')) {
      collapseDescriptionSection(wrapper, toggle);
      return;
    }

    refreshToggleLabel(wrapper, toggle);
  });
};

